function add_cart(id){
    if(cart.includes(id)){
        alert('Not available');
        
    }
    else{ 
        cart.push(id);
        localStorage.setItem('cart', JSON.stringify(cart));
        alert('your item has been add into cart')
        document.getElementById('number_count').innerHTML = cart.length;
        document.getElementById('item-count').style.display = '';

    }
}
